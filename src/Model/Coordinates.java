public class Coordinates {

    private int x;

    private int y;


    /**
     * Default constructor
     */
    public Coordinates() {
        this.x = 0;
        this.y = 0;
    }
    /**
     * Initialising constructor
     * @param x
     * @param y
     */
    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
